class SquarePatternProgram{
  public static void main(String[] args){
           for(int i = 0 ; i < 10 ; i++){
			   for(int j = i + 1 ; j - i < 10 ; j++){
				   System.out.print(" * ");
			   }
			   System.out.println(" * ");
		   }
  }
}


